"""This code prints
the values 0-9"""
print("Values from 0 to 9")
for numbers in range(10):
    print(numbers)
else:
    print("Number range elapsed")

print("")


"""The following code prints
values from 2 to 9"""
print("Values from 2 to 9") 
for numbers in range(2, 10):
    print(numbers)

else:
    print("Number range elapsed")
    
print("")

"""The following code prints
values from 0 to 18 incrementing
by 2"""
print("Values from 0 to 18 with increment value of 2")
for numbers in range(0, 20, 2):
    print(numbers)

else:
    print("Number range elapsed")
    
